//  Решение

printHello();

function printHello(){
    let age = +prompt(`Сколько Вам лет?`);

    if(age < 0) alert('Вы ввели неправильное значение');

    if(age >= 0 && age <= 12) alert('Привет, друг!');

    if(age > 12) alert('Добро пожаловать!');
}

