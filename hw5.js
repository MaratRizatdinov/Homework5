// Проверяет, являются ли переданные параметры корректными числами. (Преобразование типов и 
// isNaN()
//  помогут.)
// Если нет, то вернуть строку 'Одно или оба значения не являются числом'.
// Если оба параметра — числа, то вернуть произведение данных чисел.

// Решение

//    Разобьем задачу на 2 функции. 
//    Функция checkNum проверяет введенные пользователем числа 
//    и передает результат функции multipleNum, 
//    которая выводит соответствующие сообщения

let num1 = prompt(`Введите первое число`);
let num2 = prompt(`Введите второе число`);

multipleNum( num1 , num2);

function multipleNum(a , b){
    checkNum( a ) && checkNum( b ) ? alert(a * b):
                                     alert('Одно или оба значения не являются числом'); 
}

function checkNum(num){
    if(!isNaN(num) && num !== '' && num !== null) return true; 
    return false;
}

